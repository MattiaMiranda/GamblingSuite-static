function getCardValue(rank) {
  if (['JACK', 'QUEEN', 'KING'].includes(rank)) return 10;
  if (rank === 'ACE') return 11;
  return parseInt(RANK_DISPLAY[rank]);
}

function getHandScore(cards) {
  let score = 0;
  let aces = 0;
  for (const card of cards) {
    score += getCardValue(card.rank);
    if (card.rank === 'ACE') aces++;
  }
  while (score > 21 && aces > 0) {
    score -= 10;
    aces--;
  }
  return score;
}

function getBlackjackAdvice(playerCards, dealerCard) {
  const playerScore = getHandScore(playerCards);
  const dealerValue = getCardValue(dealerCard.rank);
  const isSoft = playerCards.some(c => c.rank === 'ACE') && playerScore <= 21;
  const canSplit = playerCards[0].rank === playerCards[1].rank;
  
  if (canSplit) {
    const pair = playerCards[0].rank;
    if (pair === 'ACE' || pair === 'EIGHT') return { action: 'SPLIT', reason: 'Always split Aces and 8s' };
    if (pair === 'TEN') return { action: 'STAND', reason: 'Never split 10s - you already have 20' };
    if (pair === 'NINE') {
      if (dealerValue >= 2 && dealerValue <= 6) return { action: 'SPLIT', reason: 'Split 9s against dealer 2-6, 8-9' };
      return { action: 'STAND', reason: 'Stand against dealer 7, 10, A' };
    }
    if (pair === 'SEVEN') {
      if (dealerValue >= 2 && dealerValue <= 7) return { action: 'SPLIT', reason: 'Split 7s against dealer 2-7' };
      return { action: 'HIT', reason: 'Hit against dealer 8+' };
    }
    if (pair === 'SIX') {
      if (dealerValue >= 2 && dealerValue <= 6) return { action: 'SPLIT', reason: 'Split 6s against dealer 2-6' };
      return { action: 'HIT', reason: 'Hit against dealer 7+' };
    }
    if (pair === 'TWO' || pair === 'THREE') {
      if (dealerValue >= 2 && dealerValue <= 7) return { action: 'SPLIT', reason: 'Split 2s/3s against dealer 2-7' };
      return { action: 'HIT', reason: 'Hit against dealer 8+' };
    }
  }
  
  if (!isSoft) {
    if (playerScore >= 17) return { action: 'STAND', reason: 'Always stand on 17+' };
    if (playerScore === 16) {
      if (dealerValue >= 2 && dealerValue <= 6) return { action: 'STAND', reason: 'Stand on 16 vs dealer 2-6' };
      return { action: 'HIT', reason: 'Hit on 16 vs dealer 7+' };
    }
    if (playerScore === 15) {
      if (dealerValue >= 2 && dealerValue <= 6) return { action: 'STAND', reason: 'Stand on 15 vs dealer 2-6' };
      return { action: 'HIT', reason: 'Hit on 15 vs dealer 7+' };
    }
    if (playerScore === 14) {
      if (dealerValue >= 2 && dealerValue <= 6) return { action: 'STAND', reason: 'Stand on 14 vs dealer 2-6' };
      return { action: 'HIT', reason: 'Hit on 14 vs dealer 7+' };
    }
    if (playerScore === 13) {
      if (dealerValue >= 2 && dealerValue <= 6) return { action: 'STAND', reason: 'Stand on 13 vs dealer 2-6' };
      return { action: 'HIT', reason: 'Hit on 13 vs dealer 7+' };
    }
    if (playerScore === 12) {
      if (dealerValue >= 4 && dealerValue <= 6) return { action: 'STAND', reason: 'Stand on 12 vs dealer 4-6' };
      return { action: 'HIT', reason: 'Hit on 12 vs dealer 2,3,7+' };
    }
    if (playerScore === 11) return { action: 'DOUBLE', reason: 'Double down on 11' };
    if (playerScore === 10) {
      if (dealerValue >= 2 && dealerValue <= 9) return { action: 'DOUBLE', reason: 'Double down on 10 vs dealer 2-9' };
      return { action: 'HIT', reason: 'Hit on 10 vs dealer 10/A' };
    }
    if (playerScore === 9) {
      if (dealerValue >= 3 && dealerValue <= 6) return { action: 'DOUBLE', reason: 'Double down on 9 vs dealer 3-6' };
      return { action: 'HIT', reason: 'Hit on 9 vs dealer 2,7+' };
    }
    if (playerScore <= 8) return { action: 'HIT', reason: 'Always hit on 8 or less' };
  }
  
  if (isSoft) {
    const aceCount = playerCards.filter(c => c.rank === 'ACE').length;
    const otherCard = playerCards.find(c => c.rank !== 'ACE');
    if (!otherCard) return { action: 'STAND', reason: 'Two aces = 12 (soft)' };
    const otherValue = getCardValue(otherCard.rank);
    const softTotal = otherValue + (aceCount > 0 ? 11 : 0) + (aceCount - 1);
    
    if (softTotal >= 19) return { action: 'STAND', reason: 'Stand on soft 19+' };
    if (softTotal === 18) {
      if (dealerValue >= 2 && dealerValue <= 6) return { action: 'DOUBLE', reason: 'Double on soft 18 vs dealer 2-6' };
      if (dealerValue >= 7 && dealerValue <= 8) return { action: 'STAND', reason: 'Stand on soft 18 vs dealer 7-8' };
      return { action: 'HIT', reason: 'Hit on soft 18 vs dealer 9+' };
    }
    if (softTotal <= 17) {
      if (dealerValue === 5 || dealerValue === 6) return { action: 'DOUBLE', reason: 'Double on soft 13-17 vs dealer 5-6' };
      return { action: 'HIT', reason: 'Hit on soft 13-17 vs dealer 2,7+' };
    }
  }
  
  return { action: 'HIT', reason: 'Default: take a card' };
}

