function getRankDisplay(rank) {
  if (appState.language === 'it') {
    const itRanks = { TWO: '2', THREE: '3', FOUR: '4', FIVE: '5', SIX: '6', SEVEN: '7', EIGHT: '8', NINE: '9', TEN: '10', JACK: 'J', QUEEN: 'Q', KING: 'K', ACE: 'A' };
    return itRanks[rank];
  }
  return RANK_DISPLAY[rank];
}

function getSuitDisplay(suit) {
  if (appState.language === 'it') {
    const itSuits = { HEARTS: 'Cuori', DIAMONDS: 'Quadri', CLUBS: 'Fiori', SPADES: 'Picche' };
    return itSuits[suit];
  }
  return suit.charAt(0) + suit.slice(1).toLowerCase();
}

