function getPokerAdvice(holeCards, tableCards) {
  const ranks = [...holeCards, ...tableCards].map(c => c.rank);
  const suits = [...holeCards, ...tableCards].map(c => c.suit);
  
  const rankCounts = {};
  ranks.forEach(r => rankCounts[r] = (rankCounts[r] || 0) + 1);
  
  const hasPair = Object.values(rankCounts).some(c => c >= 2);
  const hasThree = Object.values(rankCounts).some(c => c >= 3);
  const hasFour = Object.values(rankCounts).some(c => c >= 4);
  
  const suitCounts = {};
  suits.forEach(s => suitCounts[s] = (suitCounts[s] || 0) + 1);
  const hasFlush = Object.values(suitCounts).some(c => c >= 5);
  
  const sortedRanks = ranks.map(r => RANKS.indexOf(r)).sort((a, b) => b - a);
  let straightCount = 0;
  for (let i = 0; i < sortedRanks.length - 1; i++) {
    if (sortedRanks[i] - sortedRanks[i + 1] === 1) {
      straightCount++;
    } else if (sortedRanks[i] - sortedRanks[i + 1] > 1) {
      straightCount = 0;
    }
    if (straightCount >= 4) break;
  }
  const hasStraight = straightCount >= 4;
  
  const holeRanks = holeCards.map(c => RANKS.indexOf(c.rank));
  const isPair = holeRanks[0] === holeRanks[1];
  const isSuited = holeCards[0].suit === holeCards[1].suit;
  
  if (tableCards.length === 0) {
    if (isPair) {
      if (holeRanks[0] >= 10) return { action: 'RAISE', confidence: '95%', reason: 'Premium pair - raise or re-raise' };
      return { action: 'RAISE', confidence: '85%', reason: 'Pair - raise to isolate' };
    }
    if (holeRanks[0] >= 11 && holeRanks[1] >= 10) return { action: 'RAISE', confidence: '90%', reason: 'Strong ace-high - raise' };
    if (isSuited && holeRanks[0] >= 9 && holeRanks[1] >= 8) return { action: 'RAISE', confidence: '80%', reason: 'Connected suited connector' };
    if (holeRanks[0] >= 11 || holeRanks[1] >= 11) return { action: 'CALL', confidence: '60%', reason: ' Ace - play situationally' };
    return { action: 'FOLD', confidence: '70%', reason: 'Weak hand - fold preflop' };
  }
  
  if (hasFour) return { action: 'RAISE', confidence: '98%', reason: 'Four of a kind - bet value' };
  if (hasThree) return { action: 'RAISE', confidence: '85%', reason: 'Set/trips - bet for value' };
  if (hasStraight && hasFlush) return { action: 'RAISE', confidence: '95%', reason: 'Strong straight/flush draw' };
  
  return { action: 'CALL', confidence: '60%', reason: 'Continue based on odds' };
}

